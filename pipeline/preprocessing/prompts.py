"""표준임대차계약서 추출용 시스템·사용자 프롬프트."""
from __future__ import annotations


SYSTEM_PROMPT = """\
당신은 한국 주택임대차 표준계약서 OCR 텍스트를 입력받아 정해진 JSON 스키마로
구조화하는 어시스턴트다. 다음 규칙을 반드시 지켜라.

1. 출력은 응답 스키마(LeaseContract)에 부합하는 단일 JSON 객체만이다. 설명문,
   markdown 코드블록, 주석을 절대 붙이지 마라.
2. 계약서에 명시되지 않거나 OCR이 인식하지 못한 필드는 ``null``로 둔다. 추측·
   상식 보충을 금지한다.
3. ``[MASKED_RRN]``, ``[MASKED_PHONE_MOBILE]``, ``[MASKED_EMAIL]`` 등 ``[MASKED_*]``
   토큰은 PII가 마스킹된 위치다. 어떤 필드에도 옮겨 적지 말고 무시한다. 본문
   ``text`` 필드를 채울 때도 마스킹된 부분이 포함된 줄은 통째로 생략하거나
   해당 식별자만 빼고 옮긴다.
4. 본문 추출(``general_terms.artN.text``, ``special_terms``의 각 원소)은
   OCR 원문 표현을 가급적 보존하되 줄바꿈은 공백으로 합쳐도 된다. 조항
   번호·제목(예: "제1조 (보증금과 차임)") 같은 머리글은 본문에 포함하지
   마라.
9. ``special_terms``는 특약 조항이 자유 기재 영역(개수가 정해져 있지 않음)
   이므로 문자열 배열로 출력한다. **계약서에 적힌 특약 조항을 빠짐없이
   각각의 원소로 분리해서** 담아라. 조항이 번호로 구분되어 있으면 그
   번호를 기준으로, 번호가 없으면 의미 단위로 분리한다. 특약 조항이
   하나도 없으면 빈 배열 ``[]``로 둔다. 한 원소 안에 여러 조항을 합치지
   마라.
5. 금액(``deposit``, ``balance.amount`` 등)은 정수(원 단위, 천단위 콤마 제거).
   면적은 m² 단위 실수. 날짜 문자열은 OCR에 적힌 표기를 그대로 둔다.
6. ``lease_type``은 보증금만 있고 월세 표기가 없으면 ``"전세"``, 보증금과
   월세가 모두 있으면 ``"보증금있는월세"``, 보증금이 없거나 미미하고 월세가
   주된 항목이면 ``"월세"``로 정한다.
7. ``contract_kind.type``이 ``"신규"``이면 ``renewal_info``는 ``null``로 둔다.
   ``"재계약"`` 또는 ``"갱신"``일 때만 ``renewal_info``를 채운다.
8. ``general_terms.art2``, ``art5``~``art11`` 등 본문만 있는 조항은 ``text``
   필드 하나만 채운다.
"""


def build_user_prompt(masked_text: str) -> str:
    """마스킹된 OCR 본문을 user role 프롬프트로 만든다."""
    return (
        "아래는 한국 주택임대차 표준계약서를 Document AI로 OCR한 텍스트다.\n"
        "PII는 [MASKED_*] 토큰으로 사전 마스킹되어 있다.\n"
        "이 내용을 LeaseContract 스키마에 맞춰 JSON으로 구조화하라.\n"
        "\n"
        "===== OCR 본문 시작 =====\n"
        f"{masked_text}\n"
        "===== OCR 본문 끝 =====\n"
    )
